function mouseDragged(){
    paint(graphic)
}
function mousePressed(){
    paint(graphic)
    if(mouseX>width-200){
        if(mouseY<200){
            info.color[1]=(mouseX-width+200)/200;
            info.color[2]=mouseY/200;
            graphic2.fill(0,0,0.6)
            graphic2.noStroke()
            graphic2.rect(0,0,200,height)
            for(a=0;a<50;a++){
                for(b=0;b<50;b++){
                    graphic2.fill(info.color[0],a/50,b/50)
                    graphic2.rect(b*4,a*4,4,4)
                }
            }
            for(a=0;a<100;a++){
                graphic2.fill(a/100,1,1)
                graphic2.rect(a*2,200,2,50)
            }
        }
        if(mouseY>200&&mouseY<250){
            info.color[0]=(mouseX-width+200)/200;
            graphic2.fill(0,0,0.6)
            graphic2.noStroke()
            graphic2.rect(0,0,200,height)
            for(a=0;a<50;a++){
                for(b=0;b<50;b++){
                    graphic2.fill(info.color[0],a/50,b/50)
                    graphic2.rect(b*4,a*4,4,4)
                }
            }
            for(a=0;a<100;a++){
                graphic2.fill(a/100,1,1)
                graphic2.rect(a*2,200,2,50)
            }
        }
    }
}